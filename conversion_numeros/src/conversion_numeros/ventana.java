    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //boton de convertir
        if (jrbBinario.isSelected()) {
            String texto;
            texto = binario.getText();
            octal.setText(bin_oct(texto));
            decimal.setText(bin_dec(texto));
            hexadecimal.setText(bin_hex(texto));
        }

        if (jrbOctal.isSelected()) {
            String texto;
            texto = octal.getText();
            binario.setText(oct_bin(texto));
            decimal.setText(oct_dec(texto));
            hexadecimal.setText(oct_hex(texto));
        }

        if (jrbDeciamal.isSelected()) {
            String texto;
            texto = decimal.getText();
            binario.setText(dec_bin(texto));
            octal.setText(dec_oct(texto));
            hexadecimal.setText(dec_hex(texto));
        }

        if (jrbHexadeciamal.isSelected()) {
            String texto;
            texto = hexadecimal.getText();
            binario.setText(hex_bin(texto));
            octal.setText(hex_oct(texto));
            decimal.setText(hex_dec(texto));
        }


    }//GEN-LAST:event_jButton1ActionPerformed

