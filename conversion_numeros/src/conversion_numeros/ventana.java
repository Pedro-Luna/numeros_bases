    private void binarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_binarioKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if ((c != '0') && (c != '1')) {
            evt.consume();
        }
    }//GEN-LAST:event_binarioKeyTyped

    private void decimalKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_decimalKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            evt.consume();
        }

    }//GEN-LAST:event_decimalKeyTyped

    private void hexadecimalKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_hexadecimalKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c) && c != 'A' && c != 'B' && c != 'C' && c != 'D' && c != 'E' && c != 'F') {
            evt.consume();
        }
    }//GEN-LAST:event_hexadecimalKeyTyped

    private void octalKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_octalKeyTyped
        char c = evt.getKeyChar();
        if (c != '0' && c != '1' && c != '2' && c != '3' && c != '4' && c != '5' && c != '6' && c != '7') {
            evt.consume();
        }
    }//GEN-LAST:event_octalKeyTyped

    String dec_bin(String valor) {
        int dec = Integer.parseInt(valor);
        valor = Integer.toBinaryString(dec);
        return valor;
    }

    String dec_hex(String valor) {
        int dec = Integer.parseInt(valor);
        valor = Integer.toHexString(dec);
        return valor;
    }

    String dec_oct(String valor) {
        int dec = Integer.parseInt(valor);
        valor = Integer.toOctalString(dec);
        return valor;
    }

    String bin_dec(String valor) {
        int dec = Integer.parseInt(valor, 2);
        valor = Integer.toString(dec);
        return valor;
    }

    String bin_hex(String valor) {
        int dec = Integer.parseInt(valor, 2);
        valor = Integer.toHexString(dec);
        return valor;
    }

    String bin_oct(String valor) {
        int dec = Integer.parseInt(valor, 2);
        valor = Integer.toOctalString(dec);
        return valor;
    }

    String hex_dec(String valor) {
        int dec = Integer.parseInt(valor, 16);
        valor = Integer.toString(dec);
        return valor;
    }

    String hex_bin(String valor) {
        long dec = Integer.parseInt(valor, 16);
        valor = Long.toBinaryString(dec);
        return valor;
    }

    String hex_oct(String valor) {
        int dec = Integer.parseInt(valor, 16);
        valor = Integer.toOctalString(dec);
        return valor;
    }

    String oct_dec(String valor) {
        int dec = Integer.parseInt(valor, 8);
        valor = Integer.toString(dec);
        return valor;
    }

    String oct_bin(String valor) {
        int dec = Integer.parseInt(valor, 8);
        valor = Integer.toBinaryString(dec);
        return valor;
    }

    String oct_hex(String valor) {
        int dec = Integer.parseInt(valor, 8);
        valor = Integer.toHexString(dec);
        return valor;
    }

